    <h1>L'histoire de la CREP</h1>
    <p>A la rentrée 2013 un projet éducatif voyait le jour à l'école d'ingénieur de Polytech Lille: La CREP (Coupe Robotique des Ecoles Primaires).</p>
    <p>L'objectif de ce projet est de donner l'occasion à des écoliers de participer à un grand événement inter-écoles, réunissant initiation scientifique, défis, découvertes, échanges et jeux culturels et sportifs.</p>
    <p>Dès lors les organisateurs, enseignants et étudiants de l'école d'ingénieur Polytech Lille, en partenariat avec l'inspection de l'éducation nationale de la circonscription de Lille1-Lambersart, font tout leur possible pour organiser chaque année cet événement au sein de leur propre école.</p>
    <p>Ainsi les 2 premières éditions de la CREP ont vu la participation de centaines d'écoliers de CM1 et CM2 venant d'une douzaine d'écoles différentes, accompagnés et encadrés par des dizaines d'étudiants et d'enseignants dans une ambiance chaleureuse.</p>
    <p>L'activité principale de la CREP est un challenge ludique entre les différentes écoles sur le thème de la robotique.</p>
    <p>Tout au long de l'année chaque école doit construire et programmer un robot selon un défi commun donné à toutes les écoles avec l'aide des étudiants de Polytech dont les membres du Robotech (le club robotique de Polytech Lille) qui interviennent dans les classes pour aider les élèves dans leur démarche de programmation et de réflexion. Les élèves de chaque classe devront alors présenter ce qu'ils ont fait le jour de la CREP de leur mieux pour remporter la coupe&nbsp;!
    </p>
    <p>La CREP sera aussi l'occasion pour les écoliers de visiter l'école. Accompagnés par les étudiants, ils découvrent ce que l’on peut faire en école d'ingénieur à travers les salles de travaux pratiques et laboratoires de l'école.</p>
    <p>Un moment culture et sport est également organisé toute la journée par les clubs de l'école.
    </p>
    <p>La CREP donne ainsi l'opportunité aux élèves de manipuler, d'expérimenter de faire preuve d'esprit critique et de développer la créativité, la curiosité et l'intérêt pour un domaine technologique et scientifique.
    </p>
    <p>La présence et la participation de sponsors lors de la CREP ont permis le développement de l’événement et participe à son rayonnement.</p>
    <p>Les succès des 2 premières éditions de la CREP qui ont satisfaites l'ensemble des participants, tant les enfants et les encadrants que les sponsors et représentants de l'inspection d'Académie sont très encourageants pour la pérennité de l’événement.</p>
